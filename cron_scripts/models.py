class Animal:
    def __init__(self, id, organismo, instalacion, nombre, frecuencia, telefono, activo, fecha_nacimiento):
        self.id = id
        self.organismo = organismo
        self.instalacion = instalacion
        self.nombre = nombre
        self.frecuencia = frecuencia
        self.telefono = telefono
        self.activo = activo
        self.fecha_nacimiento = fecha_nacimiento